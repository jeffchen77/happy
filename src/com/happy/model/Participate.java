package com.happy.model;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Id;
import org.nutz.dao.entity.annotation.Table;

@Table("participate")
public class Participate {
	
	@Id
	private long id;
	
	@Column("activity_id")
	private String activityId;
	
	@Column("user_id")
	private String userId;
	
	@Column("partici_type")
	private String particiType;
	
	@Column("decision")
	private String descision;
	
	@Column("win_flag")
	private int winFlag;
	
	@Column("execute_flag")
	private int executeFlag;
	
	@Column("remark")
	private String remark;
	
	@Column("double_num")
	private int doubleNum;
	

	public int getDoubleNum() {
		return doubleNum;
	}

	public void setDoubleNum(int doubleNum) {
		this.doubleNum = doubleNum;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getActivityId() {
		return activityId;
	}

	public void setActivityId(String activityId) {
		this.activityId = activityId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getParticiType() {
		return particiType;
	}

	public void setParticiType(String particiType) {
		this.particiType = particiType;
	}

	public String getDescision() {
		return descision;
	}

	public void setDescision(String descision) {
		this.descision = descision;
	}

	public int getWinFlag() {
		return winFlag;
	}

	public void setWinFlag(int winFlag) {
		this.winFlag = winFlag;
	}

	public int getExecuteFlag() {
		return executeFlag;
	}

	public void setExecuteFlag(int executeFlag) {
		this.executeFlag = executeFlag;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}


	
	
	
}

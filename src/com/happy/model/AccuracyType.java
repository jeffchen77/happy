package com.happy.model;

import java.sql.Timestamp;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Table;

@Table("accuracy_type")
public class AccuracyType {
	
	@Column("user_id")
	private String userId;
	@Column("type_id")
	private int typeId;
	@Column("count_win")
	private int countWin;
	@Column("count_all")
	private int countAll;
	@Column("forecast_accuracy")
	private String forecastAccuracy;
	@Column("create_time")
	private Timestamp createTime;
	@Column("update_time")
	private Timestamp updateTime;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public int getTypeId() {
		return typeId;
	}
	public void setTypeId(int typeId) {
		this.typeId = typeId;
	}
	public int getCountWin() {
		return countWin;
	}
	public void setCountWin(int countWin) {
		this.countWin = countWin;
	}
	public int getCountAll() {
		return countAll;
	}
	public void setCountAll(int countAll) {
		this.countAll = countAll;
	}
	public String getForecastAccuracy() {
		return forecastAccuracy;
	}
	public void setForecastAccuracy(String forecastAccuracy) {
		this.forecastAccuracy = forecastAccuracy;
	}
	public Timestamp getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Timestamp createTime) {
		this.createTime = createTime;
	}
	public Timestamp getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Timestamp updateTime) {
		this.updateTime = updateTime;
	}
	
}
